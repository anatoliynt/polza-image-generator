        # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —É—Å–ø–µ—à–µ–Ω –ª–∏ –∑–∞–ø—Ä–æ—Å (–∫–æ–¥ 200 –∏–ª–∏ 201 = OK)
